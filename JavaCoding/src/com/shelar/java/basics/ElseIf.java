package com.shelar.java.basics;

public class ElseIf {

	public static void main(String[] args) {
		int a=20,b=20;
		if(a>b) {
			System.out.println("a is greater than b");
		}
		else if(b>a) {
			System.out.println("b is greater than a");
		}
		else {
			System.out.println("a and b are equal");
		}

	}

}
