package com.shelar.java.basics;

public class ForLoop {
	public static void main(String[] args) {
		
		
		// To Print 1 to 10 nos.
		for (int i=1;i<=10;i++) {
			System.out.println(i);		
		}
		
		
		
		// to print 1 to 10 in single line.
		for (int i=1;i<=10;i++) {
			System.out.print(i);
		}
		
	}

}
